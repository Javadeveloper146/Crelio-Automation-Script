package com.appEntity;

public class NurseEntity {
	
	private String plasmaSpecimenNumber;
	private String serumSpecimenNumber;
	private String wholeBloodSpecimenNumber;
	
	public String getPlasmaSpecimenNumber() {
		return plasmaSpecimenNumber;
	}
	public void setPlasmaSpecimenNumber(String plasmaSpecimenNumber) {
		this.plasmaSpecimenNumber = plasmaSpecimenNumber;
	}
	public String getSerumSpecimenNumber() {
		return serumSpecimenNumber;
	}
	public void setSerumSpecimenNumber(String serumSpecimenNumber) {
		this.serumSpecimenNumber = serumSpecimenNumber;
	}
	public String getWholeBloodSpecimenNumber() {
		return wholeBloodSpecimenNumber;
	}
	public void setWholeBloodSpecimenNumber(String wholeBloodSpecimenNumber) {
		this.wholeBloodSpecimenNumber = wholeBloodSpecimenNumber;
	}
	
	}
